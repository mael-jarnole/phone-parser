package fr.aerow.demo.phone;

public class PhoneFormatException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PhoneFormatException() {
	}

	public PhoneFormatException(String arg0) {
		super(arg0);
	}

	public PhoneFormatException(Throwable arg0) {
		super(arg0);
	}

	public PhoneFormatException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PhoneFormatException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
